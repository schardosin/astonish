package daemon

import (
	"time"

	"github.com/google/uuid"
	"github.com/schardosin/astonish/pkg/scheduler"
	"github.com/schardosin/astonish/pkg/store"
)

// pgSchedulerAdapter wraps a store.SchedulerStore (PostgreSQL-backed) and
// presents it as a scheduler.JobStore so the scheduler engine can operate
// on it transparently.
//
// In platform mode the daemon resolves the team's SchedulerStore at startup
// and wraps it with this adapter. The scheduler then reads/writes directly
// to PostgreSQL instead of the file-based JSON store.
type pgSchedulerAdapter struct {
	inner store.SchedulerStore
}

// newPGSchedulerAdapter creates a new adapter.
func newPGSchedulerAdapter(ss store.SchedulerStore) scheduler.JobStore {
	return &pgSchedulerAdapter{inner: ss}
}

func (a *pgSchedulerAdapter) List() []*scheduler.Job {
	storeJobs := a.inner.List()
	jobs := make([]*scheduler.Job, len(storeJobs))
	for i, sj := range storeJobs {
		jobs[i] = storeJobToSchedulerJob(sj)
	}
	return jobs
}

func (a *pgSchedulerAdapter) Get(id string) *scheduler.Job {
	sj := a.inner.Get(id)
	if sj == nil {
		return nil
	}
	return storeJobToSchedulerJob(sj)
}

func (a *pgSchedulerAdapter) GetByName(name string) *scheduler.Job {
	sj := a.inner.GetByName(name)
	if sj == nil {
		return nil
	}
	return storeJobToSchedulerJob(sj)
}

func (a *pgSchedulerAdapter) Add(job *scheduler.Job) error {
	if job.ID == "" {
		job.ID = uuid.New().String()
	}
	if job.CreatedAt.IsZero() {
		job.CreatedAt = time.Now()
	}
	if job.LastStatus == "" {
		job.LastStatus = scheduler.StatusPending
	}
	sj := schedulerJobToStoreJob(job)
	if err := a.inner.Add(sj); err != nil {
		return err
	}
	// Propagate any ID generated by the store back to the caller.
	job.ID = sj.ID
	return nil
}

func (a *pgSchedulerAdapter) Update(job *scheduler.Job) error {
	return a.inner.Update(schedulerJobToStoreJob(job))
}

func (a *pgSchedulerAdapter) Remove(id string) error {
	return a.inner.Remove(id)
}

// --- Conversion helpers ---

func storeJobToSchedulerJob(sj *store.ScheduledJob) *scheduler.Job {
	return &scheduler.Job{
		ID:   sj.ID,
		Name: sj.Name,
		Mode: scheduler.JobMode(sj.Mode),
		Schedule: scheduler.JobSchedule{
			Cron:     sj.Schedule.Cron,
			Timezone: sj.Schedule.Timezone,
		},
		Payload: scheduler.JobPayload{
			Flow:         sj.Payload.Flow,
			Params:       sj.Payload.Params,
			Instructions: sj.Payload.Instructions,
		},
		Delivery: scheduler.JobDelivery{
			Channel:        sj.Delivery.Channel,
			Target:         sj.Delivery.Target,
			Mode:           scheduler.DeliveryMode(sj.Delivery.Mode),
			MemberIDs:      sj.Delivery.MemberIDs,
			ChannelFilter:  sj.Delivery.ChannelFilter,
			MemberChannels: sj.Delivery.MemberChannels,
		},
		OwnerID:             sj.OwnerID,
		Enabled:             sj.Enabled,
		CreatedAt:           sj.CreatedAt,
		LastRun:             sj.LastRun,
		LastStatus:          scheduler.JobStatus(sj.LastStatus),
		LastError:           sj.LastError,
		NextRun:             sj.NextRun,
		ConsecutiveFailures: sj.ConsecutiveFailures,
	}
}

func schedulerJobToStoreJob(j *scheduler.Job) *store.ScheduledJob {
	return &store.ScheduledJob{
		ID:   j.ID,
		Name: j.Name,
		Mode: string(j.Mode),
		Schedule: store.JobSchedule{
			Cron:     j.Schedule.Cron,
			Timezone: j.Schedule.Timezone,
		},
		Payload: store.JobPayload{
			Flow:         j.Payload.Flow,
			Params:       j.Payload.Params,
			Instructions: j.Payload.Instructions,
		},
		Delivery: store.JobDelivery{
			Channel:        j.Delivery.Channel,
			Target:         j.Delivery.Target,
			Mode:           string(j.Delivery.Mode),
			MemberIDs:      j.Delivery.MemberIDs,
			ChannelFilter:  j.Delivery.ChannelFilter,
			MemberChannels: j.Delivery.MemberChannels,
		},
		OwnerID:             j.OwnerID,
		Enabled:             j.Enabled,
		CreatedAt:           j.CreatedAt,
		LastRun:             j.LastRun,
		LastStatus:          string(j.LastStatus),
		LastError:           j.LastError,
		NextRun:             j.NextRun,
		ConsecutiveFailures: j.ConsecutiveFailures,
	}
}

// Compile-time check.
var _ scheduler.JobStore = (*pgSchedulerAdapter)(nil)
